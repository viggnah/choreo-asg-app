package org.wso2.demo.loyalty;

public class LoyaltyPoint {
    String name;
    Integer totalNumberOfPoints;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getTotalNumberOfPoints() {
        return totalNumberOfPoints;
    }

    public void setTotalNumberOfPoints(Integer totalNumberOfPoints) {
        this.totalNumberOfPoints = totalNumberOfPoints;
    }
}
